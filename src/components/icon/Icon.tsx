export default function Icon({
  name,
  color,
  ...props // Spread additional props
}: {
  name: keyof typeof iconPaths; // Valid icon names
  size?: number;
  color?: string;
} & React.SVGProps<SVGSVGElement>) {
  const IconComponent = iconPaths[name];

  if (!IconComponent) {
    console.error(`Icon "${name}" does not exist.`);
    return <FallbackIcon name={name} color={color} {...props} />;
  }

  try {
    const svgProps = {
      ...props,
    };

    if (color) {
      // To override hardcoded fill attributes in SVGs, we need to use more specific CSS
      svgProps.style = {
        ...svgProps.style,
        color: color, // For currentColor references
      };

      // Add CSS that will enforce color on child paths, regardless of hardcoded fills
      svgProps.className = `${props.className || ""} custom-icon-color`.trim();

      // Create or append to existing style element to inject our CSS
      if (
        typeof document !== "undefined" &&
        !document.getElementById("icon-color-styles")
      ) {
        const styleEl = document.createElement("style");
        styleEl.id = "icon-color-styles";
        styleEl.innerHTML = `
          .custom-icon-color path,
          .custom-icon-color rect,
          .custom-icon-color circle,
          .custom-icon-color polygon {
            fill: currentColor !important;
          }
          .custom-icon-color line,
          .custom-icon-color polyline {
            stroke: currentColor !important;
          }
        `;
        document.head.appendChild(styleEl);
      }
    }

    // Check if IconComponent is a React component or an imported SVG
    if (typeof IconComponent === 'object' && IconComponent.src) {
      // It's an imported SVG (Next.js image object), render an img tag
      return (
        <img 
          src={IconComponent.src} 
          alt={`${name} icon`}
          width={props.width || props.size || 24}
          height={props.height || props.size || 24}
          style={{
            ...svgProps.style,
            display: 'inline-block',
          }}
          className={svgProps.className}
        />
      );
    } else {
      // It's a React component, render normally
      return <IconComponent {...svgProps} />;
    }
  } catch (error) {
    console.error(`Error rendering icon "${name}":`, error);
    return <FallbackIcon name={name} color={color} {...props} />;
  }
}
